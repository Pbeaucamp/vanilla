package bpm.gateway.runtime2;

public class GatewayRuntimeException extends Exception {

	public GatewayRuntimeException() {
		super();
		
	}

	public GatewayRuntimeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public GatewayRuntimeException(String arg0) {
		super(arg0);
		
	}

	public GatewayRuntimeException(Throwable arg0) {
		super(arg0);
		
	}

}
