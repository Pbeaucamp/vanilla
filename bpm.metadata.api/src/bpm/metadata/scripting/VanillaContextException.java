package bpm.metadata.scripting;

public class VanillaContextException extends Exception{

	public VanillaContextException() {
		super();
		
	}

	public VanillaContextException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public VanillaContextException(String message) {
		super(message);
		
	}

	public VanillaContextException(Throwable cause) {
		super(cause);
		
	}

}
