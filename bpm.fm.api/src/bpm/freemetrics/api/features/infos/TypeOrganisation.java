package bpm.freemetrics.api.features.infos;

public class TypeOrganisation {
	
	private int id;
	private String name;
	
	public TypeOrganisation() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
