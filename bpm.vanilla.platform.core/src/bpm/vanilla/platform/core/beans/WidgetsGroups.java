package bpm.vanilla.platform.core.beans;

public class WidgetsGroups {

	private int widgetId;
	private int idGroup;
	private int idWidgetGroup;

	public int getWidgetId() {
		return widgetId;
	}

	public void setWidgetId(int widgetId) {
		this.widgetId = widgetId;
	}

	public int getIdGroup() {
		return idGroup;
	}

	public void setIdGroup(int idGroup) {
		this.idGroup = idGroup;
	}

	public int getIdWidgetGroup() {
		return idWidgetGroup;
	}

	public void setIdWidgetGroup(int idWidgetGroup) {
		this.idWidgetGroup = idWidgetGroup;
	}

}
